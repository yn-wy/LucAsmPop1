// iostream header included to use cout
#include <iostream>

// string header included to use string
#include <string>

// used to avoid typing 'std::' prefix
using namespace std;

// declare and define the entrypoint function 'main'
int main() {
    // declare a string called 'name' and assign it the value "Yan Naing Wai Yan"
    string name = "Yan Naing Wai Yan";

    // declare an integer called 'age' and assign it the value of 19
    int age = 19;

    // displays the name and age
    cout << "Name: " << name << ", age: " << age << '\n';
}
